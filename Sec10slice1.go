//Program to implement a slice in GO

package main

import "fmt"

func main() {
	//aggregate => data structures having values of the same type
	//composite literal => data structures having data of different types
	x := []int{4, 5, 6, 7, 8}
	fmt.Println(x)
}

//eg of aggregate data structures: arrays,slices,lists
//eg of composite data structures: struct

//a SLICE allows you to group values of the SAME TYPE

//OUTPUT:
//[4 5 6 7 8]
