//Simple program that illustrates the use of the major data types - 'int','string' and 'boolean'

package main

import "fmt"

func main() {
	x := 42
	y := "James Bond"
	z := true
	fmt.Println(x, y, z)
	fmt.Println(x)
	fmt.Println(y)
	fmt.Println(z)
}

//OUTPUT:
// 42 James Bond true
// 42
// James Bond
// true
